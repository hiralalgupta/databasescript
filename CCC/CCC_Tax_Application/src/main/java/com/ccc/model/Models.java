package com.ccc.model;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="MODELS")
public class Models {
	
	@Id
	private int modelId;	
	private String trim;
	private int year;
	private int vehicleModelId;
	private int modelMakeId;	
	private int obj_body;
	
	public int getModelId() {
		return modelId;
	}
	public void setModelId(int modelId) {
		this.modelId = modelId;
	}
	public String getTrim() {
		return trim;
	}
	public void setTrim(String trim) {
		this.trim = trim;
	}
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getObj_body() {
		return obj_body;
	}
	public void setObj_body(int obj_body) {
		this.obj_body = obj_body;
	}
	public int getModelMakeId() {
		return modelMakeId;
	}
	public void setModelMakeId(int modelMakeId) {
		this.modelMakeId = modelMakeId;
	}
	public int getVehicleModelId() {
		return vehicleModelId;
	}
	public void setVehicleModelId(int vehicleModelId) {
		this.vehicleModelId = vehicleModelId;
	}
	
}
